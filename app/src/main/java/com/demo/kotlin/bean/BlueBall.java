package com.demo.kotlin.bean;

public class BlueBall {
    private int one;
    private int two;


    public int getOne() {
        return one;
    }

    public void setOne(int one) {
        this.one = one;
    }

    public int getTwo() {
        return two;
    }

    public void setTwo(int two) {
        this.two = two;
    }


    public BlueBall(int one, int two) {
        this.one = one;
        this.two = two;
    }
}